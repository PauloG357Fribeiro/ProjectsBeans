        public void readJTable() throws SQLException {
            DefaultTableModel modelo = (DefaultTableModel) jProduto.getModel();
            modelo.setNumRows(0); produtoDAO mdao = new produtoDAO();
            for (tableProduto m: mdao.read()) {
                modelo.addRow(new Object[]{
                    m.getId(),
                    m.getNome(),
                    m.getValor(),
                    m.getUnidade()});
        }
    }
        public void readJTableForDesc (String desc) throws SQLException {
            DefaultTableModel modelo = (DefaultTableModel) jProduto.getModel();
            modelo.setNumRows(0);
            produtoDAO pdao = new produtoDAO();
            for (tableProduto m: pdao.readForDesc(desc)){
               modelo.addRow(new Object[]{
                   m.getId(),
                   m.getNome(),
                   m.getValor(),
                   m.getUnidade()  });
    }
        }

