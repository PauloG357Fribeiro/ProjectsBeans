    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(jProduto.getSelectedRow() != -1){
        tableProduto m = new tableProduto();
        produtoDAO dao = new produtoDAO(); 
        m.setId((int)jProduto.getValueAt(jProduto.getSelectedRow(), 0));
        dao.delete(m);
        txtNome.setText("");
        txtValor.setText("");
        txtUnidade.setSelectedItem("");
        try{
          readJTable();
        } catch (SQLException ex) {
            Logger.getLogger(viewTableProduto.class.getName()).log(Level.SEVERE, null, ex);
           }
        }else JOptionPane.showMessageDialog(null, "Selecione um produto para excluir");
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if(jProduto.getSelectedRow() != -1){
        tableProduto m = new tableProduto();
        produtoDAO dao = new produtoDAO(); 
        m.setNome(txtNome.getText());
        m.setValor(txtValor.getText());
        m.setUnidade((String) txtUnidade.getSelectedItem());
        m.setId((int)jProduto.getValueAt(jProduto.getSelectedRow(), 0));
        dao.update(m);
        txtNome.setText("");
        txtValor.setText("");
        txtUnidade.setSelectedItem("");
        try{
          readJTable();
        } catch (SQLException ex) {
            Logger.getLogger(viewTableCadastro.class.getName()).log(Level.SEVERE, null, ex);
           }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

